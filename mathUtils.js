export function add(thingOne, thingTwo) {
    return thingOne + thingTwo;
}

export function sub(thingOne, thingTwo) {
    return thingOne - thingTwo;
}

export function mult(thingOne, thingTwo) {
    return thingOne * thingTwo;
}

export function div(thingOne, thingTwo) {
    return thingOne / thingTwo;
}